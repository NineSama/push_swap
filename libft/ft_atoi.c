#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	valeur;

	i = 0;
	sign = 1;
	valeur = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		valeur = valeur * 10 + (str[i] - '0');
		i++;
	}
	if (sign == -1)
		return (valeur * -1);
	return (valeur);
}
